/**
 * Classic graphic-LCD 5×7 font (Pascal Stang; mirrored in Paul Stoffregen LedDisplay).
 * Each glyph = 5 bytes: one per column, bits 0–6 = rows with bit 0 = top scanline.
 * @see https://github.com/PaulStoffregen/LedDisplay/blob/master/font5x7.h
 */

export const GLYPH_COLS = 5;
export const GLYPH_ROWS = 7;

/**
 * Each font “pixel” becomes an N×N block of mosaic squares (N=3 → strokes are 3 squares thick).
 * With 5×7 source and N=3, the bitmap is exactly 15×21.
 */
export const GLYPH_BLOCK_SCALE = 3;

export const GLYPH_EXPANDED_COLS = GLYPH_COLS * GLYPH_BLOCK_SCALE;
export const GLYPH_EXPANDED_ROWS = GLYPH_ROWS * GLYPH_BLOCK_SCALE;

/** One row per scanline (top → bottom); each row is left → right. True = lit “pixel”. */
export type Glyph5x7Grid = boolean[][];

/** 0x20 (space) through 0x7F (last slot is custom in source file: arrow / DEL slot). */
