function assertFontLength(): void {
  if (FONT_5X7.length !== BYTES) {
    throw new Error(`font5x7: expected ${BYTES} bytes, got ${FONT_5X7.length}`);
  }
}
assertFontLength();

/** Raw 7×5 grid for printable ASCII; space/out-of-range → empty. */
export function glyph5x7FromChar(ch: string): Glyph5x7Grid {
  const code = ch.codePointAt(0);
  const c = code !== undefined && code >= 0x20 && code <= 0x7f ? code : 0x20;
  const base = (c - 0x20) * 5;
  const grid: Glyph5x7Grid = [];
  for (let row = 0; row < GLYPH_ROWS; row++) {
    const line: boolean[] = [];
    for (let col = 0; col < GLYPH_COLS; col++) {
      const byte = FONT_5X7[base + col] ?? 0;
      line.push(((byte >> row) & 1) !== 0);
    }
    grid.push(line);
  }
  return grid;
}

/** Embed a 5×7 glyph in a letter cell: each dot is expanded to {@link GLYPH_BLOCK_SCALE}² squares, centered. */
export function glyph5x7PlacedInCell(
  ch: string,
  cellCols: number,
  cellRows: number
): boolean[][] {
  const g = glyph5x7FromChar(ch);
  const scale = GLYPH_BLOCK_SCALE;
  const expW = GLYPH_EXPANDED_COLS;
  const expH = GLYPH_EXPANDED_ROWS;
  const ox = Math.floor((cellCols - expW) / 2);
  const oy = Math.floor((cellRows - expH) / 2);
  const out: boolean[][] = Array.from({ length: cellRows }, () => Array(cellCols).fill(false));

  for (let r = 0; r < GLYPH_ROWS; r++) {
    const row = g[r];
    if (!row) continue;
    for (let c = 0; c < GLYPH_COLS; c++) {
      if (!row[c]) continue;
      for (let dr = 0; dr < scale; dr++) {
        const dstY = oy + r * scale + dr;
        if (dstY < 0 || dstY >= cellRows) continue;
        const outRow = out[dstY]!;
        for (let dc = 0; dc < scale; dc++) {
          const dstX = ox + c * scale + dc;
          if (dstX >= 0 && dstX < cellCols) {
            outRow[dstX] = true;
          }
        }
      }
    }
  }
  return out;
}
